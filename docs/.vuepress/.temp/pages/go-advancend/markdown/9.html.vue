<h3 id="grafana" tabindex="-1"><a class="header-anchor" href="#grafana" aria-hidden="true">#</a> grafana</h3>
<ul>
<li><a href="https://grafana.com" target="_blank" rel="noopener noreferrer">官方网站<ExternalLinkIcon/></a></li>
<li><a href="https://github.com/grafana/" target="_blank" rel="noopener noreferrer">github组织地址<ExternalLinkIcon/></a></li>
</ul>
<p>Grafana 是一个可视化工具，简单点说就是用来展示数据的。它和Zabbix、Prometheus 有本质区别，在于它不能解决监控问题，仅用于展示。也就是说，在监控领域，Grafana 需要配合 Zabbix、Prometheus 等工具一起使用，以获取数据源。</p>
<p>Grafana 官方是这么介绍 Grafana 的：grafana是用于可视化大型测量数据的开源程序，他提供了强大和优雅的方式去创建、共享、浏览数据。dashboard中显示了你不同metric数据源中的数据。</p>
<p>Grafana 官方还对 Grafana 的适用场景以及基本特征作了介绍：</p>
<p>grafana最常用于因特网基础设施和应用分析，但在其他领域也有机会用到，比如：工业传感器、家庭自动化、过程控制等等。
grafana有热插拔控制面板和可扩展的数据源，目前已经支持Graphite、InfluxDB、OpenTSDB、Elasticsearch。
这里需要留意的是，上面官方列举的数据源都是时序型数据库。这也透露出 Grafana 的另一大适用性：Grafana 一般是配合时序数据库做数据展示的。</p>
